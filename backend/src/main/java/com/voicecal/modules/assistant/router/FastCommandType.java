package com.voicecal.modules.assistant.router;

/**
 * 快速规则路由类型。
 *
 * <p>用于表示用户输入命中的快速规则类别。快速规则主要处理高频、安全、
 * 确定性的日程查询类指令，命中后可绕过大模型，直接调用后端业务服务返回结果。</p>
 */
public enum FastCommandType {

    /**
     * 查询今天的日程安排。
     *
     * <p>适用于“今天有什么安排”“今天有哪些日程”“今日安排”等用户指令。</p>
     */
    TODAY_EVENTS,

    /**
     * 查询明天的日程安排。
     *
     * <p>适用于“明天有什么安排”“明天有哪些日程”“明日安排”等用户指令。</p>
     */
    TOMORROW_EVENTS,

    /**
     * 查询本周的日程安排。
     *
     * <p>适用于“本周有什么安排”“这周有哪些日程”“查看本周日程”等用户指令。</p>
     */
    WEEK_EVENTS,

    /**
     * 查询指定时间范围内的空闲时间。
     *
     * <p>适用于“今天下午有没有空”“明天上午有空吗”“周五晚上有没有空闲时间”等用户指令。</p>
     */
    FREE_TIME
}